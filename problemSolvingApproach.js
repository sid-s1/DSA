// 1. Devise a plan for solving problems
// 2. Master common problem solving patterns (some recipes)

// 1. Understand the Problem -
// Say it's a new problem completely and you are unable to solve it right off the bat
// Instinct is to get started IMMEDIATELY 
// BUT FIRST, ask yourself some questions FIRST -
// Can I restate the problem in my OWN WORDS?
// What are the inputs that go into the problem?
// What are the outputs that should come from the solution to the problem?
// Can the outputs be determined from the inputs? In other words, do I have enough information to solve the problem? (You may not be able to answer this until you set about solving the problem; that's okay, it's still worth considering the question at this early stage)
// How should I label the important pieces of data that are a part of the problem?

// THEN, write some simple examples - easiest use cases
// Then, more complex cases
// Then, explore examples with empty inputs
// Then, explore examples with invalid inputs

// Then start explicitly writing down the steps you need to take - pseudo coding
// This forces you to think about the code you'll write before you write it, and helps you catch any lingering conceptual issues or misunderstandings before you dive in and have to worry about syntax and all
// All of the above helps in demonstrating that even if you are unable to do the concrete implementation of your logic, you were able to think through the problem atleast, and knew WHERE you were going
// LAY OUT THE PROCESS AT THE START - even if you can't finish the problem, you may pass the interview

// Solve the problem; if you can't, solve a simpler problem
// SIMPLIFY - Fine the core difficulty in what you're trying to do - the thing that you are panicking about. Temporarily ignore that difficulty. Write a simplified solution. Then incorporate that difficulty back in. Don't fixate on that difficulty early on - don't freeze because of that. Start somewhere

// Refactoring questions -
// Can you check the result?
// Can you derive the result differently?
// Can you understand it at a glance?
// Can you use the result or method for some other problem?
// Can you improve the performance of your solution?
// Can you think of other ways to refactor?
// How have other people solved this problem?